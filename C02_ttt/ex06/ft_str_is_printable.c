int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (((unsigned)str[i] < 32) || ((unsigned)str[i] >= 127))
			return (0);
		i++;
	}
	return (1);
}
